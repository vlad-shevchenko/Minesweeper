package window.main;

/**
 * <p>
 * Enumeration of game difficulties - Easy, Medium, Hard, Special.
 * </p>
 *
 * @author Vlad
 */
public enum GameDifficulty {

    /**
     * <p>
     * Easy game mode - field 9x9 with 10 bombs.
     * </p>
     *
     * @author Vlad
     */
    Easy,

    /**
     * <p>
     * Medium game mode - field 15x15 with 25 bombs.
     * </p>
     *
     * @author Vlad
     */
    Medium,

    /**
     * <p>
     * Hard game mode - field 25x25 with 70 bombs.
     * </p>
     *
     * @author Vlad
     */
    Hard,

    /**
     * <p>
     * Special game mode - field size and number of bombs specified in settings.
     * </p>
     *
     * @author Vlad
     */
    Special

}
